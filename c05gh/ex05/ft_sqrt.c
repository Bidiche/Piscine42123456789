int		ft_sqrt(int nb)
{
	unsigned int i;

	i = 0;
	if (nb <= 0 || nb == 2)
		return (0);
	while (i * i < (unsigned int)nb)
		i++;
	if (i * i == (unsigned int)nb)
		return (i);
	return (0);
}
