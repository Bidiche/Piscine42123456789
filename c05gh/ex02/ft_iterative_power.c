int		ft_iterative_power(int nb, int power)
{
	int i;
	int r;

	i = power;
	r = nb;
	if (i < 0)
		return (0);
	if (i == 0)
		return (1);
	while (i > 1)
	{
		r = r * nb;
		i--;
	}
	return (r);
}
