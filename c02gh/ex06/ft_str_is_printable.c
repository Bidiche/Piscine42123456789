#include <stdio.h>

int		ft_str_is_printable(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (32 <= str[i] && str[i] < 127)
			i++;
		else
			return (0);
	}
	return (1);
}
